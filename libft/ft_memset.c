#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	unsigned char	*rv;

	rv = (unsigned char *)b;
	i = 0;
	while (i < len)
		rv[i++] = (unsigned char)c;
	return (rv);
}
