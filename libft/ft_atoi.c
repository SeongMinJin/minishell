#include "libft.h"

int	ft_atoi(const char *str)
{
	int	rv;
	int	sign;

	rv = 0;
	sign = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		rv *= 10;
		rv += (*str - '0') * sign;
		str++;
	}
	return (rv);
}
